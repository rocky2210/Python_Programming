# Define two variables for comparison
a = 10
b = 20

# Equal to (==) operator
print("a == b:", a == b)  # False

# Not equal to (!=) operator
print("a != b:", a != b)  # True

# Greater than (>) operator
print("a > b:", a > b)    # False

# Less than (<) operator
print("a < b:", a < b)    # True

# Greater than or equal to (>=) operator
print("a >= b:", a >= b)  # False

# Less than or equal to (<=) operator
print("a <= b:", a <= b)  # True

# Output
"""
a == b: False
a != b: True
a > b: False
a < b: True
a >= b: False
a <= b: True
"""