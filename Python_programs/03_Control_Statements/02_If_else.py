# If else statement

"""
    If else statement:
        the if statement is often accompanied by the else statement to provide an alternative 
        action if the condition in the if statement evaluates to False. 
"""

y = 3
if y > 5:
    print("y is greater than 5")
else:
    print("y is not greater than 5")  #This block will be executed

"""
    Output:
        y is not greater than 5
"""