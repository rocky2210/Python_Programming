# os.path.split(): Splits the path into the directory and filename.


# os.path.splitext(): Splits the path into the base and extension.


# os.environ: Provides access to the environment variables as a dictionary.


# os.path.getsize(): Returns the size, in bytes, of the file at the given path.


# os.path.abspath(): Returns the absolute path of the given path.


# os.path.relpath(path, start="."): Returns a relative filepath to start from the given path


# os.path.realpath(): Returns the canonical path of the specified filename, eliminating any symbolic links encountered in the path.


# os.path.exists(): Checks whether the given path exists.


# os.path.samefile(path1, path2): Returns True if the paths point to the same file or directory.


# os.walk(top, topdown=True, onerror=None, followlinks=False): Generates the file names in a directory tree by walking either top-down or bottom-up.